#include <unistd.h>

void	ft_putchar(char j)
{
	write(1, &j, 1);
}

void	ft_putnbr(int nb)
{
	if (nb < 0 && nb != -2147483648)
	{
		ft_putchar('-');
		nb = nb * (-1);
	}
	if (nb == -2147483648)
		write(1, "-2147483648", 11);
	if (nb > 9)
		ft_putnbr(nb / 10);
	if (nb != -2147483648)
		ft_putchar(nb % 10 + '0');
}
