#include <unistd.h>

int	ft_atoi(char *str)
{
	int	i;
	int	s;
	int	m;

	i = 0;
	s = 0;
	m = 1;
	while (str[i] == '\t' || str[i] == '\n' || str[i] == '\v' || str[i] == '\f'
		|| str[i] == '\r' || str[i] == ' ')
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			m *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		s = (str[i] - '0') + (s * 10);
		i++;
	}
	return (s * m);
}
