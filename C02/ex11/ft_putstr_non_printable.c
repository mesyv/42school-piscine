#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

int	ft_is_printable(char c)
{
	if (c < ' ' || c > '~')
		return (0);
	return (1);
}

void	ft_hex(unsigned char c)
{
	ft_putchar('\\');
	ft_putchar("0123456789abcdef"[c / 16]);
	ft_putchar("0123456789abcdef"[c % 16]);
}

void	ft_putstr_non_printable(char *str)
{
	unsigned int	i;

	i = 0;
	while (str[i])
	{
		if (ft_is_printable(str[i]) == 1)
			ft_putchar(str[i]);
		else
			ft_hex(str[i]);
		i++;
	}
}
